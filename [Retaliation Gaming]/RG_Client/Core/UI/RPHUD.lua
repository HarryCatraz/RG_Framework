Citizen.CreateThread(function()
    while true do
        Citizen.Wait(2)
        local Health = (GetEntityHealth(GetPlayerPed(-1)) - 100)
        textHealth(math.floor(Health))
        textStamina(math.floor(GetPlayerSprintStaminaRemaining(GetPlayerPed(-1))))
        textMoney(Config.money)
        textBank(Config.bank)
    end
end)